//if-else statement in java
//conditional operators:  >   <   ==   !=   >=   <=   &&   ||
public class ifelse {
    public static void main(String[] args){
        color="Red";
        if(color=="red"){
            System.out.printf("Color: Red");
        }
        else if (color=="Blue"){
            System.out.printf("Color: Blue");
        }
        else{
            System.out.printf("Color: NA");
        }
    }
}
