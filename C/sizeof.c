/*WAP to determine the size of different datatypes*/
#include<stdio.h>
int main()
{
    printf("Size of integer is- %ld\n", sizeof(int));
    printf("Size of float is- %ld\n", sizeof(float));
    printf("Size of double is- %ld\n", sizeof(double));
    printf("Size of long double is- %ld\n", sizeof(long double));
    printf("Size of character is- %ld\n", sizeof(char));
    return 0;
}